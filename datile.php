  <h3 class="text-center bg-dark text-white">Anti Ragging</h3>
  <p>G.P.Dwarahat has been able to maintain a "Ragging Free Campus" over the last couple of years. In our endeavors for ensuring the same we have received invaluable support from the students and their guardians. Today, ragging has become synonymous with teasing, terror, harassment, cruelty, fear , physical and mental torture and abuse of human rights of fresher which is resulting in numerous avoidable deaths, suicides, group clashes/conflicts and a large number of fresher dropouts.</p> 
  <p>G.P.Dwarahat considers ragging as a cognizable and heinous offence and does not hesitate to take stern action against the offenders with a view to maintain "Ragging Free Campus".</p>
</div>
</div>
</div>
<p><strong>Definition and punishment for the ragging are given as under</strong></p>
<p>1. Any conduct by any student or students, whether by word spoken or written or by an act, which has the effect of teasing, treating or handling with rudeness a fresher or any other student.</p>
<p>2. Exploiting students for completing academic task and financial extortion.</p>
<p>3. Any act of physical abuse including all its variants - sexual abuse, homosexual assaults, and stripping, forcing obscene and lewd acts, gestures, causing bodily harm or any other danger to health of a person.</p>
<p>4. Any act that prevents disrupts or disturbs the regular academic activity of other students.</p>

<p><strong>Punishment For Ragging</strong></p>

<p>1. Some of the punishments for students directly or indirectly involved in the ragging and its abetment are given as under":</p>
<p>2. Cancellation of Admission.</p>
<p>3. Suspension from attending classes.</p>
<p>4. Withholding / withdrawing of scholarship / fellowship and other benefits.</p>
<p>5. Debarring from appearing for any test / examination/placement activities organized by the institute.</p>
<p>6. Withholding of results.</p>
<p>7. Lodging FIR against offender.</p>
<p>8. Debarring from representing the institution in any National or International meet, tournament and festival.</p>
<p>9. Suspension / Expulsion from the hostel.</p>
<p>10.Rustication from the institute for varying periods.</p>
<p>11. Expulsion from the institution and consequent debarring from admission to any other institution.</p>
<p>12. Fine up to Rs. 2,50,000/- (up to 2.5 lacs).</p>
<p>13. Rigorous Imprisonment for up to 3 years (under court of law).</p><br>
