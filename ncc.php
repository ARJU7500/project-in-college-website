<h4 class="text-center bg-dark text-white">National Cadet Corps</h4>
<p>The National Cadet Corps is the youth wing of Armed Forces with its headquarters at New Delhi, Delhi, India. It is open to school and college students on voluntary basis. National Cadet Corps is a Tri-Services Organisation, comprising the Army, Navy and Air Wing, engaged in grooming the youth of the country into disciplined and patriotic citizens. The National Cadet Corps in India is a voluntary organisation which recruits cadets from high schools, higher secondary, colleges and universities all over India. The Cadets are given basic military training in small arms and drill. The officers and cadets have no liability for active military service once they complete their course.</p>
</div>
</div>
</div>
<p>The NCC in India was formed the National Cadet Corps Act of 1948 . It can be traced back to the ‘University Corps’, which was created under the Indian Defence Act 1917, with the objective to make up for the shortage in the Army. In 1920, when the Indian Territorial Act was passed, the ‘University Corps’ was replaced by the University Training Corps (UTC). The aim was to raise the status of the UTC and make it more attractive to the youth. The UTC Officers and cadets dressed like the army. It was a significant step towards</p>
<p>the Indianisation of armed forces. It was rechristened as UOTC so the National Cadet Corps can be considered as a successor of the University Officers Training Corps (UOTC) which was established by British Government in 1942. During World War II, the UOTC never came up to the expectations set by the British. This led to the idea that some better schemes should be formed, which could train more young men in a better way, even during peace. A committee headed by H N Kunzru recommended a cadet organisation to be established in schools and colleges at a national level. The National Cadet Corps Act was accepted by the Governor General and on 15 July 1948 the National Cadet Corps came into existence<a href="https://en.wikipedia.org/wiki/National_Cadet_Corps_(India)" target="_blank">Lernmore.....</a></p>
