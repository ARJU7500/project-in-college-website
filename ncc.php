<h4 class="text-center bg-dark text-white">National Service Scheme</h4>
<p> The National Service Scheme (NSS) is an Indian government-sponsored flagship for public service program conducted by the Ministry of Youth Affairs and Sports of the Government of India. Popularly known as NSS, the scheme was launched in Gandhiji's Centenary year in 1969. Aimed at developing student's personality through community service, NSS is a voluntary association of young people in Colleges, Universities and at +2 level working for a campus-community (esp. Villages) linkage.</p>
</div>
</div>
</div>
<p> After independence the University Grants Commission, headed by S. Radhakrishnan, recommended the introduction of voluntary national service in academic institutions. This idea was again considered by the Central Advisory Board of Education (CABE) at its meeting in January, 1950; after examining the idea and the experiences of other countries in this field, the board recommended that students and teachers should devote time to voluntary manual work. In the draft first Five-Year Plan adopted by the government in 1952, the need for social and labour service by Indian students for one year was stressed. In 1958 Jawaharlal Nehru, in a letter to the chief ministers, considered the idea of social service as a prerequisite for graduation. He directed the Ministry of Education to formulate a suitable scheme for the introduction of national service into academic institutions<a href="https://en.wikipedia.org/wiki/National_Service_Scheme"target="_blank">Lernmore....</a></p>
