<p>Hostel is provided for GIRLS and BOYS inside the campus. Students are provided with adequate facilities to make them feel at home. Their stay in the hostel enables them to imbibe a different lifestyle and involve them in activities Including Yoga, Meditation, Sports, Music etc, suiting individuals taste and preferences.</p>
<p><strong> HOSTEL IS EQUIPPED WITH.</strong>
<p>1. 24 hours electricity and water supply.</p>
<p>2. Green refreshing surroundings to relax.</p>
<p>3. First aid facilities and visit of doctors on call.</p>
<p>4. Airy and well furnished rooms.</p>
<p>5. Hygienic, tasty and nourishing foods to inmates.</p>

<p><strong>HOSTEL RULES</strong></p>
<p>1. Hostel fee is to be paid in advance during the starting of each session.</p>
<p>2. Hostel fee will not be refundable in case of expulsion from hostel by the management.</p>
<p>3. Shifting of furniture from one room to another is not allowed.</p>
<p>4. No interchange of room is permitted without the written permission of the hostel warden.</p>
<p>5. During class timings the students are not allowed to stay in the hostel rooms..</p>
<p>6. Morning assembly and exercises are compulsory for all hostlers, however relaxation/exemption may be given in case of physical disability on case-to-case basis.</p>
